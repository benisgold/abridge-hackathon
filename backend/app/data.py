def find_procedure(code: str) -> ProcedureSeed | None:
    """Look up by exact code, or by the first code of a composite entry."""
    normalized = code.strip().replace(" ", "")
    for procedure in PROCEDURES:
        if procedure.code == normalized:
            return procedure
        if normalized in procedure.code.split(","):
            return procedure
    return None
