"""Hardcoded seed data for the cost-comparison demo.

All pricing here is SYNTHETIC. The hospital names and locations are real, but
every dollar figure is generated by app.pricing and bears no relationship to
what these institutions actually charge.
"""

from dataclasses import dataclass

from .models import Hospital, Procedure


@dataclass(frozen=True)
class HospitalSeed:
    id: str
    name: str
    address: str
    city: str
    state: str
    zip_code: str
    lat: float
    lng: float
    phone: str
    # Synthetic pricing knobs: academic medical centers run high, community
    # hospitals low, and self-pay discounts vary independently of list price.
    price_multiplier: float
    self_pay_discount_rate: float

    def to_model(self) -> Hospital:
        return Hospital(
            id=self.id,
            name=self.name,
            address=self.address,
            city=self.city,
            state=self.state,
            zip_code=self.zip_code,
            lat=self.lat,
            lng=self.lng,
            phone=self.phone,
        )


@dataclass(frozen=True)
class ProcedureSeed:
    code: str
    name: str
    description: str
    base_price: int
    # Share of total fees billed by the physician rather than the facility.
    # Lab draws are almost entirely facility; surgery is not.
    physician_share: float
    # None means "we don't know" — reserved for codes not in this catalog.
    medicare_covered: bool | None = True

    def to_model(self) -> Procedure:
        return Procedure(
            code=self.code,
            name=self.name,
            description=self.description,
            medicare_covered=self.medicare_covered,
        )


